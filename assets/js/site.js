function animateHeadings() {
  document.querySelectorAll('.hero h1, .page-hero h1').forEach((title) => {
    const text = title.textContent.trim();
    if (!text || title.dataset.animatedText === text) return;
    title.dataset.animatedText = text;
    title.setAttribute('aria-label', text);
    title.innerHTML = text
      .split(/\s+/)
      .map((word, index) => `<span class="kinetic-word" aria-hidden="true" style="--word-index:${index}">${word}</span>`)
      .join(' ');
  });
}

function updateStaticText(language) {
  const copy = STATIC_COPY[language] || STATIC_COPY.en;
  document.querySelectorAll('.skip-link').forEach((element) => { element.textContent = copy.skip; });
  document.querySelector('.mobile-toggle')?.setAttribute('aria-label', copy.menu);

  const pageTitleKey = document.body.classList.contains('theme-ximen') ? 'ximenPageTitle'
    : document.body.classList.contains('theme-changchun') ? 'changchunPageTitle'
    : document.body.classList.contains('theme-travel') ? 'travelPageTitle'
    : document.body.classList.contains('theme-booking') ? 'bookingPageTitle'
    : document.body.classList.contains('theme-contact') ? 'contactPageTitle'
    : 'homeTitle';
  document.title = copy[pageTitleKey];

  if (document.body.classList.contains('theme-contact')) {
    const blocks = document.querySelectorAll('.contact-block');
    const ximenAddress = blocks[0]?.querySelector('p');
    const changchunAddress = blocks[1]?.querySelector('p');
    if (ximenAddress) ximenAddress.innerHTML = copy.ximenAddress;
    if (changchunAddress) changchunAddress.innerHTML = copy.changchunAddress;
  }
}

