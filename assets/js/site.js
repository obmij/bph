function currentLanguage() {
  const stored = storage.get(STORAGE_KEY);
  if (stored && I18N[stored]) return stored;
  const documentLanguage = document.documentElement.lang;
  return I18N[documentLanguage] ? documentLanguage : 'zh-Hant';
}

function animateHeadings() {
  document.querySelectorAll('.hero h1, .page-hero h1').forEach((title) => {
    const text = title.textContent.trim();
    if (!text || title.dataset.animatedText === text) return;
    title.dataset.animatedText = text;
    title.setAttribute('aria-label', text);
    title.innerHTML = text
      .split(/\s+/)
      .map((word, index) => `<span class="kinetic-word" aria-hidden="true" style="--word-index:${index}">${word}</span>`)
      .join(' ');
  });
}

function applyLanguage(language) {
  const selectedLanguage = I18N[language] ? language : 'zh-Hant';
  const dictionary = { ...FALLBACK, ...I18N[selectedLanguage] };
  document.documentElement.lang = selectedLanguage;
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) element.textContent = value;
  });
  document.querySelectorAll('.language-select').forEach((select) => { select.value = selectedLanguage; });
  storage.set(STORAGE_KEY, selectedLanguage);
  animateHeadings();
  refreshBookingFloat(selectedLanguage);
  refreshContactActions(selectedLanguage);
}

function initNavigation() {
  const toggle = document.querySelector('.mobile-toggle');
  toggle?.addEventListener('click', () => document.body.classList.toggle('menu-open'));
  document.querySelectorAll('.mobile-nav a').forEach((link) => {
    link.addEventListener('click', () => document.body.classList.remove('menu-open'));
  });
}

function initRevealAnimations() {
  const elements = document.querySelectorAll('.reveal');
  if (!('IntersectionObserver' in window)) {
    elements.forEach((element) => element.classList.add('is-visible'));
    return;
  }
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });
  elements.forEach((element) => observer.observe(element));
}

function initImageMotion() {
  const media = document.querySelector('.hero-media img, .page-hero > img');
  if (!media || matchMedia('(prefers-reduced-motion: reduce)').matches) return;
  let ticking = false;
  const update = () => {
    media.style.translate = `0 ${Math.min(scrollY, 700) * 0.055}px`;
    ticking = false;
  };
  window.addEventListener('scroll', () => {
    if (!ticking) {
      requestAnimationFrame(update);
      ticking = true;
    }
  }, { passive: true });
}

