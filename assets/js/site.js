function refreshBookingFloat(language = currentLanguage()) {
  const aside = document.querySelector('[data-booking-float]');
  if (!aside) return;
  const copy = bookingCopy(language);
  aside.querySelectorAll('[data-booking-label="title"]').forEach((element) => { element.textContent = copy.title; });
  aside.querySelectorAll('[data-booking-label="book"]').forEach((element) => { element.textContent = copy.book; });
  aside.querySelectorAll('[data-booking-label="call"]').forEach((element) => { element.textContent = copy.call; });
  aside.querySelectorAll('[data-booking-location]').forEach((element) => { element.textContent = copy[element.dataset.bookingLocation]; });
  aside.querySelector('.booking-close')?.setAttribute('aria-label', copy.close);
  aside.querySelector('.booking-back')?.setAttribute('aria-label', copy.back);
  const selectedKey = aside.dataset.selectedBranch;
  const heading = aside.querySelector('[data-booking-heading]');
  if (heading) heading.textContent = selectedKey ? copy[selectedKey] : copy.title;
  const selected = aside.querySelector('[data-booking-selected]');
  if (selected && selectedKey) selected.textContent = copy[selectedKey];
}

