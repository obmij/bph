function bookingCopy(language = currentLanguage()) {
  return BOOKING_COPY[language] || BOOKING_COPY.en;
}

function resetBookingFloat(aside) {
  const branchView = aside.querySelector('[data-booking-view="branches"]');
  const actionView = aside.querySelector('[data-booking-view="actions"]');
  const back = aside.querySelector('.booking-back');
  branchView.hidden = false;
  actionView.hidden = true;
  back.hidden = true;
  aside.dataset.selectedBranch = '';
  refreshBookingFloat();
}

function createBookingFloat() {
  document.querySelector('[data-quick-services]')?.remove();
  document.body.classList.remove('has-quick-services');
  if (document.querySelector('[data-booking-float]')) return;

  injectBookingStyles();
  const copy = bookingCopy();
  const aside = document.createElement('aside');
  aside.className = 'booking-float';
  aside.dataset.bookingFloat = '';
  aside.innerHTML = `
    <button class="booking-float-toggle" type="button" aria-expanded="false" aria-controls="booking-float-panel">
      <span class="booking-float-toggle-icon" aria-hidden="true">↗</span>
      <span data-booking-label="title">${copy.title}</span>
    </button>
    <div id="booking-float-panel" class="booking-float-panel" hidden>
      <div class="booking-float-header">
        <button class="booking-back" type="button" aria-label="${copy.back}" hidden>←</button>
        <strong class="booking-float-heading" data-booking-heading>${copy.title}</strong>
        <button class="booking-close" type="button" aria-label="${copy.close}">×</button>
      </div>
      <div class="booking-view booking-view-branches" data-booking-view="branches">
        <button class="booking-branch booking-branch-changchun" type="button" data-booking-branch="changchun">
          <span data-booking-location="changchun">${copy.changchun}</span><span class="booking-branch-arrow" aria-hidden="true">›</span>
        </button>
        <button class="booking-branch booking-branch-ximen" type="button" data-booking-branch="ximen">
          <span data-booking-location="ximen">${copy.ximen}</span><span class="booking-branch-arrow" aria-hidden="true">›</span>
        </button>
      </div>
      <div class="booking-view booking-view-actions" data-booking-view="actions" hidden>
        <div class="booking-selected" data-booking-selected></div>
        <div class="booking-actions">
          <a class="booking-action booking-action-book" data-booking-action="book" target="_blank" rel="noopener noreferrer"><span aria-hidden="true">↗</span><span data-booking-label="book">${copy.book}</span></a>
          <a class="booking-action booking-action-call" data-booking-action="call"><span aria-hidden="true">☎</span><span data-booking-label="call">${copy.call}</span></a>
        </div>
      </div>
    </div>`;

  document.body.appendChild(aside);
  document.body.classList.add('has-booking-float');

  const toggle = aside.querySelector('.booking-float-toggle');
  const panel = aside.querySelector('.booking-float-panel');
  const close = aside.querySelector('.booking-close');
  const back = aside.querySelector('.booking-back');
  const branchView = aside.querySelector('[data-booking-view="branches"]');
  const actionView = aside.querySelector('[data-booking-view="actions"]');
  const selected = aside.querySelector('[data-booking-selected]');
  const bookLink = aside.querySelector('[data-booking-action="book"]');
  const callLink = aside.querySelector('[data-booking-action="call"]');

  const setOpen = (open) => {
    toggle.setAttribute('aria-expanded', String(open));
    panel.hidden = !open;
    if (!open) resetBookingFloat(aside);
  };

  toggle.addEventListener('click', () => setOpen(toggle.getAttribute('aria-expanded') !== 'true'));
  close.addEventListener('click', () => setOpen(false));
  back.addEventListener('click', () => resetBookingFloat(aside));

  aside.querySelectorAll('[data-booking-branch]').forEach((button) => {
    button.addEventListener('click', () => {
      const key = button.dataset.bookingBranch;
      const location = LOCATIONS[key];
      if (!location) return;
      aside.dataset.selectedBranch = key;
      selected.textContent = bookingCopy()[key];
      bookLink.href = location.booking;
      callLink.href = location.phoneHref;
      callLink.setAttribute('aria-label', `${bookingCopy().call} ${location.phoneText}`);
      branchView.hidden = true;
      actionView.hidden = false;
      back.hidden = false;
      refreshBookingFloat();
    });
  });

  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') setOpen(false);
  });
  document.addEventListener('click', (event) => {
    if (!aside.contains(event.target)) setOpen(false);
  });
}

function refreshBookingFloat(language = currentLanguage()) {
  const aside = document.querySelector('[data-booking-float]');
  if (!aside) return;
  const copy = bookingCopy(language);
  aside.querySelectorAll('[data-booking-label="title"]').forEach((element) => { element.textContent = copy.title; });
  aside.querySelectorAll('[data-booking-label="book"]').forEach((element) => { element.textContent = copy.book; });
  aside.querySelectorAll('[data-booking-label="call"]').forEach((element) => { element.textContent = copy.call; });
  aside.querySelectorAll('[data-booking-location]').forEach((element) => { element.textContent = copy[element.dataset.bookingLocation]; });
  aside.querySelector('.booking-close')?.setAttribute('aria-label', copy.close);
  aside.querySelector('.booking-back')?.setAttribute('aria-label', copy.back);
  const selectedKey = aside.dataset.selectedBranch;
  const heading = aside.querySelector('[data-booking-heading]');
  if (heading) heading.textContent = selectedKey ? copy[selectedKey] : copy.title;
  const selected = aside.querySelector('[data-booking-selected]');
  if (selected && selectedKey) selected.textContent = copy[selectedKey];
}

function contactActions(key) {
  const copy = bookingCopy();
  const location = LOCATIONS[key];
  const actions = document.createElement('div');
  actions.className = 'contact-direct-actions';
  actions.dataset.contactActions = key;
  actions.innerHTML = `
    <a class="button secondary" href="${location.phoneHref}">${location.phoneText}</a>
    <a class="button outline" href="${location.street}" target="_blank" rel="noopener noreferrer" data-map-action="street">${copy.street}</a>
    <a class="button" href="${location.directions}" target="_blank" rel="noopener noreferrer" data-map-action="directions">${copy.directions}</a>`;
  return actions;
}

function enhanceContactPage() {
  if (!document.body.classList.contains('theme-contact')) return;
  const blocks = document.querySelectorAll('.contact-block');
  if (blocks[0] && !blocks[0].querySelector('[data-contact-actions]')) blocks[0].appendChild(contactActions('ximen'));
  if (blocks[1] && !blocks[1].querySelector('[data-contact-actions]')) blocks[1].appendChild(contactActions('changchun'));
}

function refreshContactActions(language = currentLanguage()) {
  const copy = bookingCopy(language);
  document.querySelectorAll('[data-map-action="street"]').forEach((element) => { element.textContent = copy.street; });
  document.querySelectorAll('[data-map-action="directions"]').forEach((element) => { element.textContent = copy.directions; });
}

function enableOfficialBooking() {
  if (!document.body.classList.contains('theme-booking')) return;
  const form = document.querySelector('.form-card');
  if (!form) return;
  form.removeAttribute('onsubmit');
  form.addEventListener('submit', (event) => {
    event.preventDefault();
    const select = form.querySelector('select');
    const key = select?.selectedIndex === 1 ? 'changchun' : 'ximen';
    window.location.assign(LOCATIONS[key].booking);
  });
}

function init() {
  const language = storage.get(STORAGE_KEY) || 'zh-Hant';
  applyLanguage(language);
  document.querySelectorAll('.language-select').forEach((select) => {
    select.addEventListener('change', (event) => applyLanguage(event.target.value));
  });
  initNavigation();
  initRevealAnimations();
  initImageMotion();
  createBookingFloat();
  enhanceContactPage();
  enableOfficialBooking();
  refreshBookingFloat(language);
  refreshContactActions(language);
  const year = document.getElementById('year');
  if (year) year.textContent = String(new Date().getFullYear());
}

document.addEventListener('DOMContentLoaded', init);
