function validateTranslationCoverage() {
  const requiredKeys = Object.keys(FALLBACK);
  Object.entries(I18N).forEach(([language, dictionary]) => {
    const missing = requiredKeys.filter((key) => !Object.prototype.hasOwnProperty.call(dictionary, key));
    if (missing.length) console.error(`Missing ${language} translations: ${missing.join(', ')}`);
  });
}

